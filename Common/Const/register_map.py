# ESP32-S3 Common Register Addresses

# GPIO Registers
GPIO_OUT_REG = 0x60004004          # GPIO output register
GPIO_OUT_W1TS_REG = 0x60004008     # GPIO output set register
GPIO_OUT_W1TC_REG = 0x6000400C     # GPIO output clear register
GPIO_ENABLE_REG = 0x60004020       # GPIO output enable register
GPIO_ENABLE_W1TS_REG = 0x60004024  # GPIO output enable set register
GPIO_ENABLE_W1TC_REG = 0x60004028  # GPIO output enable clear register
GPIO_IN_REG = 0x6000403C           # GPIO input register
GPIO_STATUS_REG = 0x60004044       # GPIO interrupt status register
GPIO_STATUS_W1TS_REG = 0x60004048  # GPIO interrupt status set register
GPIO_STATUS_W1TC_REG = 0x6000404C  # GPIO interrupt status clear register

